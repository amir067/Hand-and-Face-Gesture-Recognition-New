package com.mcs.FaceGestureApp.Api;

public class API {
    private String API_Key;
    private String URL;

    public API() {
    }

    public String getAPI_Key() {
        return API_Key;
    }

    public void setAPI_Key(String API_KEY) {
        this.API_Key = API_KEY;
    }

    public String getUrl() {
        return URL;
    }

    public void setUrl(String url) {
        URL = url;
    }
}
